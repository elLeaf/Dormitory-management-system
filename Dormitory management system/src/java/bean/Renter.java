public class Renter {
    
    private String renter_id;
    private String renter_fname;
    private String renter_lname;
    private String renter_gender;
    private String renter_age;
    private String renter_phone;
    private String renter_address;

    public String getRenter_id() {
        return renter_id;
    }

    public void setRenter_id(String renter_id) {
        this.renter_id = renter_id;
    }

    public String getRenter_fname() {
        return renter_fname;
    }

    public void setRenter_fname(String renter_fname) {
        this.renter_fname = renter_fname;
    }

    public String getRenter_lname() {
        return renter_lname;
    }

    public void setRenter_lname(String renter_lname) {
        this.renter_lname = renter_lname;
    }

    public String getRenter_gender() {
        return renter_gender;
    }

    public void setRenter_gender(String renter_gender) {
        this.renter_gender = renter_gender;
    }

    public String getRenter_age() {
        return renter_age;
    }

    public void setRenter_age(String renter_age) {
        this.renter_age = renter_age;
    }

    public String getRenter_phone() {
        return renter_phone;
    }

    public void setRenter_phone(String renter_phone) {
        this.renter_phone = renter_phone;
    }

    public String getRenter_address() {
        return renter_address;
    }

    public void setRenter_address(String renter_address) {
        this.renter_address = renter_address;
    }
    
    
    
}
